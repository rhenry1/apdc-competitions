#!/usr/bin/env node
// Generates the <noscript> fallback schedule for each competition page from
// that page's own authored COMPETITION_CONFIG/SCHEDULE — so the fallback can
// never drift from the real data (W3.1). Regenerate after any schedule edit:
//   node scripts/build-noscript.js
// CI enforces this via tests/noscript-fallback.spec.js, which fails if the
// committed <noscript> block doesn't match what this script would produce.
'use strict';

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const PAGES = [
  path.join(__dirname, '..', 'nationals-2026', 'index.html'),
  path.join(__dirname, '..', 'regionals-spring-2027', 'index.html'),
];

const START = '<!-- NOSCRIPT-FALLBACK:START (generated by scripts/build-noscript.js — do not hand-edit) -->';
const END = '<!-- NOSCRIPT-FALLBACK:END -->';

function escapeHtml(str) {
  return String(str == null ? '' : str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

// Pulls the inline `const COMPETITION_CONFIG = {...}; const SCHEDULE = {...};`
// script out of the page and evaluates it in a sandbox (no DOM, no other
// globals) to get plain data back — the same authored literals the real
// engine renders from, so the fallback can't diverge from the live schedule.
function extractConfigAndSchedule(html) {
  const m = html.match(/<script>\s*\nconst COMPETITION_CONFIG[\s\S]*?<\/script>/);
  if (!m) throw new Error('Could not find the inline COMPETITION_CONFIG/SCHEDULE script block.');
  const code = m[0].replace(/^<script>/, '').replace(/<\/script>$/, '');
  const sandbox = {};
  vm.createContext(sandbox);
  vm.runInContext(code + '\nthis.__out = { config: COMPETITION_CONFIG, schedule: SCHEDULE };', sandbox);
  return sandbox.__out;
}

function renderItem(item) {
  if (item.type === 'routine') {
    const bits = [
      item.time,
      item.entry ? '#' + item.entry : '',
      item.title,
      [item.formatTag, item.style, item.stage ? 'Stage ' + item.stage : ''].filter(Boolean).join(', '),
      item.dancers,
    ].filter(Boolean).map(escapeHtml);
    return '<li>' + bits.join(' &mdash; ') + '</li>';
  }
  // category / meta / awards rows are plain schedule notes (breaks, awards,
  // title lineups) — keep them, just without the icon styling.
  return '<li class="ns-note">' + escapeHtml(item.text || '') + '</li>';
}

function renderDay(dayKey, dayConf, items) {
  if (!items || !items.length) return '';
  const title = escapeHtml((dayConf && dayConf.title) || dayKey);
  return (
    '<section><h3>' + title + '</h3><ul>' +
    items.map(renderItem).join('') +
    '</ul></section>'
  );
}

function renderNoscriptBlock(config, schedule) {
  const days = (config.dayButtons || []).map(({ key }) =>
    renderDay(key, (config.days || {})[key], schedule[key])
  ).filter(Boolean).join('');

  return (
    START + '\n' +
    '<noscript>\n' +
    // Hide the interactive chrome that does nothing without JS (the engine
    // builds/wires all of it); show the plain schedule listing instead.
    '<style>#filter-bar,#offset-bar,#schedule-container,#no-results{display:none!important}</style>\n' +
    '<div class="ns-fallback">\n' +
    '<p>JavaScript is required for the interactive schedule (search, filters, favorites). ' +
    'Scheduled times are subject to change. Here is the full published schedule:</p>\n' +
    days + '\n' +
    '</div>\n' +
    '</noscript>\n' +
    END
  );
}

// The engine overwrites #header-title's textContent with COMPETITION_CONFIG.name
// on load (see schedule-engine.js), so keeping the static default in sync is a
// no-op when JS runs — but it means the <h1> reads the real competition name
// instead of a generic placeholder on the (rare) load where JS never runs.
function syncHeaderTitle(html, config) {
  const re = /(<h1 id="header-title">)[^<]*(<\/h1>)/;
  if (!re.test(html)) return html;
  return html.replace(re, (_m, open, close) => open + escapeHtml(config.name) + close);
}

function buildForFile(filePath, { check = false } = {}) {
  const html = fs.readFileSync(filePath, 'utf8');
  const { config, schedule } = extractConfigAndSchedule(html);
  const block = renderNoscriptBlock(config, schedule);

  const re = new RegExp(
    START.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '[\\s\\S]*?' + END.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  );
  if (!re.test(html)) {
    throw new Error(
      `${path.relative(process.cwd(), filePath)}: no NOSCRIPT-FALLBACK markers found. ` +
      `Add "${START}\\n${END}" inside <main>, right after its opening tag.`
    );
  }
  let updated = html.replace(re, block);
  updated = syncHeaderTitle(updated, config);
  const changed = updated !== html;
  if (changed && !check) fs.writeFileSync(filePath, updated);
  return changed;
}

if (require.main === module) {
  const check = process.argv.includes('--check');
  let anyChanged = false;
  for (const file of PAGES) {
    const changed = buildForFile(file, { check });
    anyChanged = anyChanged || changed;
    const rel = path.relative(process.cwd(), file);
    if (check) {
      console.log(changed ? `STALE  ${rel}` : `OK     ${rel}`);
    } else {
      console.log(changed ? `WROTE  ${rel}` : `OK     ${rel} (already current)`);
    }
  }
  if (check && anyChanged) {
    console.error('\nOne or more noscript fallbacks are stale. Run: node scripts/build-noscript.js');
    process.exit(1);
  }
}

module.exports = { extractConfigAndSchedule, renderNoscriptBlock, buildForFile, escapeHtml, START, END };
